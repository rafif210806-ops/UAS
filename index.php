    <p class="mb-3" style="line-height:1.6; font-family: 'Lora', serif; text-align: justify;">
      Pada tahun 1979 Universitas Sumatera Utara ditetapkan sebagai salah satu dari enam Universitas/Institut yang memperoleh proyek pembangunan Pendidikan Politeknik Tahap I dari DIKTI dengan nama Politeknik Universitas Sumatera Utara(Politeknik USU). Pembangunan ini dibiayai oleh World Bank. Pada tahap pertama, pendidikan yang dikembangkan adalah bidang keteknikan dengan dukungan tenaga ahli dari Swiss yang ditempatkan di Medan. Selanjutnya pada tahap kedua dibangun pendidikan bidang tata niaga yang didukung oleh tenaga ahli dan fasilitas dari Australia.
