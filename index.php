    <h2>Sejarah Politeknik Negeri Medan</h2>

    <p>
        Pada tahun 1979 Universitas Sumatera Utara ditetapkan sebagai salah satu dari
        enam Universitas/Institut yang memperoleh proyek pembangunan Pendidikan
        Politeknik Tahap I dari DIKTI dengan nama Politeknik Universitas Sumatera Utara
        (Politeknik USU). Pembangunan ini dibiayai oleh World Bank.
    </p>

    <p>
        Pada tahap pertama, pendidikan yang dikembangkan adalah bidang keteknikan
        dengan dukungan tenaga ahli dari Swiss yang ditempatkan di Medan.
        Selanjutnya pada tahap kedua dibangun pendidikan bidang tata niaga yang
        didukung oleh tenaga ahli dan fasilitas dari Australia. Program ini mulai
        diselenggarakan pada tahun 1986.
    </p>

    <p>
        Pembangunan Politeknik USU dan politeknik lainnya merupakan proyek pemerintah
        melalui Depdikbud Ditjen DIKTI yang dilaksanakan oleh Polytechnic Education
        Development Center (PEDC) di Bandung.
    </p>

    <p>
        Melalui Surat Keputusan Mendikbud Nomor 084/O/1997 tentang pendirian
        Politeknik Negeri Medan, Politeknik USU secara resmi menjadi politeknik
        mandiri dengan nama Politeknik Negeri Medan. Ketentuan ini diperbaharui
        melalui Surat Keputusan Mendiknas Nomor 130/O/2002 tentang organisasi dan
        tata kerja Politeknik Negeri Medan.
    </p>

    <p class="lokasi">
        Walaupun telah berdiri sendiri, Politeknik Negeri Medan masih menempati
        kampus Universitas Sumatera Utara yang beralamat di Jalan Almamater No. 1
        Kampus USU Medan atau melalui Jalan Tridharma No. 2 Kampus USU (Pintu 4).
    </p>
